def suma(num1, num2):
    sum_cal = num1 + num2
    print("Resultado de la Suma es: ",end=""); print(sum_cal)

def resta(num1, num2):
    res_cal = num1 - num2
    print("Resultado de la Resta es: ",end=""); print(res_cal)

def mult(num1, num2):
    mult_cal = num1 * num2
    print("Resultado de la Multiplicacion es: ",end=""); print(mult_cal)

def divi(num1, num2):
    div_cal = num1 - num2
    print("Resultado de la Division es: ",end=""); print(div_cal)        